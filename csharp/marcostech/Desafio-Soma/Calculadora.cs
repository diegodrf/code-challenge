namespace marcostech.Calculadora
{
    public class Calculadora
    {        
        ///<param name="numA"> número inteiro a ser somado </param>
        ///<param name="numB"> número inteiro a ser somado </param>
        ///<returns> resultado da soma de dois inteiros </returns>
        public int Somar(int numA, int numB) {
            return numA + numB;
        }
    }
}